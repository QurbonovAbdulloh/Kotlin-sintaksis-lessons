package Lesson

import java.lang.Math.nextDown
import java.lang.Math.pow
import kotlin.math.*

fun main() {

    println(E)             //taqriban 2.71ga teng
    println(PI)            //3.14
    println(abs(-10))   //absolyut, ya'ni modul qiymat chiqarib berad |-10|=10
    println(sin(90.0))  //sin
    println(cos(90.0))  //cos
    println(tan(90.0))  //tan
    println(ceil(4.1))  // 4.1 + -> 5
    println(floor(4.1)) // 4.1 - -> 4
    println(log(25.0, 5.0))
    println(max(10, 15))  //Ikkita sondan kattasini topib beradi
    println(min(10, 15))  //Ikkita sondan kichigini topib beradi
    println(10.0.pow(2.0))   //Sonni darajaga ko'tarish
    println(round(10.6))     //sonni yaxlitlash
    println(sign(10.0))   //Sonning belgisini qaytaradi. 10 -> 1, -10 -> -1, 0 -> 0
    println(ln(0.0))      //Natural logarifm
    println(10.0.nextDown()) //O'zidan keyingi eng kichik sonni ko'rsatib beradi
    println(10.0.nextUp())   //O'zidan keyingi eng katta sonni ko'rsatib beradi
    println(8.0.nextTowards(9.0))





}